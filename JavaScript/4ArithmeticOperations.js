// Define two numbers
let a = 10;
let b = 3;

// Addition (+)
let sum = a + b;
console.log("Addition (a + b): " + sum); // Output: Addition (a + b): 13

// Subtraction (-)
let difference = a - b;
console.log("Subtraction (a - b): " + difference); // Output: Subtraction (a - b): 7

// Multiplication (*)
let product = a * b;
console.log("Multiplication (a * b): " + product); // Output: Multiplication (a * b): 30

// Division (/)
let quotient = a / b;
console.log("Division (a / b): " + quotient); // Output: Division (a / b): 3.3333333333333335

// Modulus (%)
let remainder = a % b;
console.log("Modulus (a % b): " + remainder); // Output: Modulus (a % b): 1

// Exponentiation (**)
let power = a ** b;
console.log("Exponentiation (a ** b): " + power); // Output: Exponentiation (a ** b): 1000

// Increment (++)
let increment = a;
increment++;
console.log("Increment (a++): " + increment); // Output: Increment (a++): 11

// Decrement (--)
let decrement = b;
decrement--;
console.log("Decrement (b--): " + decrement); // Output: Decrement (b--): 2
